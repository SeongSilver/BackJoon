import java.util.Scanner;

public class BJ_while_10952 {

	public static void main(String[] args) {
		// TODO 자동 생성된 메소드 스텁
		Scanner sc = new Scanner(System.in);
		
		while(true) {
			int A = sc.nextInt();
			int B = sc.nextInt();
			sc.close();
			
			System.out.println(A+B);
		}
		
	}

}
